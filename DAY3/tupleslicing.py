#TUPLE SLICING

student=("Kashyap",23,"New Delhi","Btech in CSE")

print(student[1:4]) #element from index 1 to 3 will be printed
print(student[:4]) #element from 0 index to 3 will be printed
print(student[:])  #element from 0 index to index=len(tuple) will be printed
print(student[1:])  #element from 1 index to index=len(tuple) will be printed
print(student[-4:-1]) #element from index -4 to -2 will be printed
